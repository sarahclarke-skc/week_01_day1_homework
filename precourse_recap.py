number = input("What number between 1 and 10 am I thinking about?")
if int(number) == 8:
    print("Wow! You're a mind reader!")
elif int(number) == 7:
    print("You're close, but not quite!")
elif int(number) == 9:
    print("You're too high!")
else:
    print("You're not telepathic in the slightest!")